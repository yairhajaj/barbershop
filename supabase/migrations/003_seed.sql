INSERT INTO staff_hours (staff_id, day_of_week, start_time, end_time, is_working)
SELECT s.id, d.day, '09:00'::time, '19:00'::time, CASE WHEN d.day = 6 THEN false ELSE true END
FROM staff s
CROSS JOIN (VALUES (0),(1),(2),(3),(4),(5),(6)) AS d(day)
